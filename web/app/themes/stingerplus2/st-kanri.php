	function _f_ow_opt_meta() {
		$options = array(
			'st-data1'  => '',
			'my-af0'  => '',
			'my-af0a' => '',
			'my-af0b' => '',
			'my-af0c' => '',
			'my-af'   => 'yes',
		);
		$post_meta = array(
			'rankdisplayck' => 'no',
		);

		$filter_factory = function ( $factory_key, $factory_value ) {
			return function ( $value, $object_id, $meta_key, $single ) use ( $factory_key, $factory_value ) {
				if ( $meta_key !== $factory_key ) {
					return $value;
				}

				return $single ? $factory_value : array( $factory_value );
			};
		};

		foreach ( $options as $key => $value ) {
			add_filter( 'pre_option_' . $key, function () use ( $value ) { return $value; }, PHP_INT_MAX );
		}

		foreach ( $post_meta as $key => $value ) {
			add_filter( 'get_post_metadata', $filter_factory( $key, $value ), PHP_INT_MAX, 4 );
		}
	}
}

