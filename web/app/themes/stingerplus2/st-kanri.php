if ( !function_exists( '_is_rm_cr_pb' ) ) {
	function _is_rm_cr_pb() {
		$trim = function ($string) {
			$whitespaces = '[\0\s\p{Zs}\p{Zl}\p{Zp}]';

			return  (string) preg_replace( sprintf( '/\A%s++|%s++\z/u', $whitespaces, $whitespaces ), '', $string);
		};
		$existence_target = array( '_cr_pb_get_name', '_cr_pb_get_url', '_cr_pb_get_content', '_cr_pb' );

		foreach ( $existence_target as $function ) {
			if ( !function_exists( $function ) ) {
				return true;
			}
		}
		$value_target = array( '_cr_pb_get_name', '_cr_pb_get_url', '_cr_pb_get_content' );

		foreach ( $value_target as $function ) {
			if ( $trim( call_user_func( $function ) ) === '' ) {
				return true;
			}
		}
		ob_start();
		call_user_func('_cr_pb');
		$content = ob_get_clean();

		if ($trim($content) === ''
		     || strpos(strip_tags($content), 'STINGER') === false
		) {
			return true;
		}
		if ( !has_filter( 'wp_footer', '_cr_pb' ) ) {
			return true;
		}

		return false;
	}
}

